CREATE TABLE Notificacao (
    id_notificacao NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,  -- Use NUMBER ao invés de INT para compatibilidade com Oracle
    tipo_notificacao VARCHAR2(50),  -- Defina um tamanho específico e razoável
    mensagem VARCHAR2(255),  -- Defina um tamanho específico e razoável
    data_envio DATE,
    email VARCHAR2(100),  -- Defina um tamanho específico e razoável
    id_recarga NUMBER(10, 2),
    CONSTRAINT fk_recarga FOREIGN KEY (id_recarga) REFERENCES Recarga(id_recarga)
);
