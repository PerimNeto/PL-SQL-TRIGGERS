CREATE TABLE Recarga (
    id_recarga NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,  -- Usado para geração automática de IDs no Oracle
    valor_recarga NUMBER(10, 2),  -- Usa NUMBER para valores monetários
    metodo_pagamento VARCHAR2(50),  -- Usa VARCHAR2 para Oracle
    data_recarga DATE,
    status VARCHAR2(50),
    id_Cartao_Transporte NUMBER(38, 0),  -- Coluna para chave estrangeira
    CONSTRAINT fk_cartao_transporte1 FOREIGN KEY (id_Cartao_Transporte) REFERENCES Cartao_Transporte(id_Cartao_Transporte)
);
--DROP TABLE recarga;

CREATE TABLE Cartao_Transporte (
    id_Cartao_Transporte NUMBER(38, 0) PRIMARY KEY,
    numero_cartao NUMBER(10, 2),  -- Usa NUMBER para identificadores únicos
    tipo_cartao VARCHAR2(50),  -- Usa VARCHAR2 e define um comprimento adequado
    saldo_cartao NUMBER(10, 2),  -- Usa NUMBER para valores monetários
    data_emissao DATE
);

CREATE TABLE Linha (
    id_linha NUMBER(38, 0) PRIMARY KEY,  -- Usa NUMBER para identificadores únicos
    nome_linha VARCHAR2(100),  -- Define um comprimento adequado
    rota VARCHAR2(255),  -- Define um comprimento adequado
    tempo_estimado VARCHAR2(50),  -- Usa VARCHAR2 para armazenar tempo estimado em formato textual
    localizacao_atual VARCHAR2(255),  -- Define um comprimento adequado
    data_ultima_atualizacao DATE,  -- Usa DATE para armazenar a última atualização
    horario_inicio TIMESTAMP,  -- Usa TIMESTAMP para armazenar horário
    horario_fim TIMESTAMP  -- Usa TIMESTAMP para armazenar horário
);

CREATE TABLE Extrato (
    id_extrato NUMBER PRIMARY KEY,  -- Corrigido o nome e usado NUMBER
    valor_tarifa NUMBER(10, 2),  -- Usa NUMBER para valores monetários
    data_pagamento DATE,
    id_Cartao_Transporte NUMBER(38, 0),  -- Ajustado para NUMBER correto
    id_Linha NUMBER(38, 0),  -- Ajustado para NUMBER correto
    id_recarga NUMBER,
    CONSTRAINT fk_cartao_transporte FOREIGN KEY (id_Cartao_Transporte) REFERENCES Cartao_Transporte(id_Cartao_Transporte),
    CONSTRAINT fk_linha FOREIGN KEY (id_Linha) REFERENCES Linha(id_linha)  -- Referencia correta da tabela Linha
);
