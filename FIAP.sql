CREATE TRIGGER AtualizarSaldoAposRecarga
AFTER INSERT ON Recarga
FOR EACH ROW
BEGIN
    UPDATE Cartao_Transporte
    SET saldo_cartao = saldo_cartao + :NEW.valor_recarga
    WHERE id_Cartao_Transporte = :NEW.id_Cartao_Transporte;
END;

